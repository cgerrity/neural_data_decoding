# ───────────────────────── Augmentation (shared) parity ─────────────────────────


def test_augmentation_shared_schedule_applies_to_every_std(
    case_D: Any, bundle: CurriculumBundle,
) -> None:
    """The flat augmentation schedule applies identically to every std_* parameter."""
    epochs = _vec(case_D.epochs).astype(int)
    matlab_vals = _vec(case_D.aug_values_base1)
    for name in ("std_channel_offset", "std_white_noise",
                 "std_random_walk", "std_time_shift"):
        py_vals = _trace(bundle, "load", name, epochs)
        np.testing.assert_allclose(
            py_vals, matlab_vals, rtol=1e-12, atol=1e-12,
            err_msg=f"load.{name} drifted from MATLAB augmentation schedule",
        )
